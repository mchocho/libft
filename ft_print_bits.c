#include "libft.h"

void    print_bits(int octet)
{
    int z;
	int oct;

	z = 1;
	oct = (int)octet;

	z <<= 16;
    while (z > 0)
    {
        if (oct & z)
            write(1, "1", 1);
        else
            write(1, "0", 1);
        z >>= 1;
    }
}
