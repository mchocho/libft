char *ft_strrchr(const char *s, int c) 
{
    int i;
    int j;

    i = 0;
    j = -1;
    
    while(s[i] != '\0') 
	{
        if (s[i] == c) 
		{
            j = i;
        }
        i++;
    }
	if (c == '\0') {
		return ((char *)s + i);
	} else if (j > -1) 
	{
        return ((char *)s + j);
    } else {
		return (NULL);
	}
}
